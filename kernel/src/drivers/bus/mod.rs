// Шина (Bus) — это среда, через которую CPU общается с устройствами.
//
// Типы шин в реальном железе:
//   PCI / PCIe  — основная шина для GPU, NVMe, сетевых карт
//   USB         — периферия (клавиатуры, мыши, флешки)
//   I2C / SPI   — сенсоры, embedded
//   Platform    — виртуальная шина для встроенных устройств
//
// В Linux каждый тип шины реализует struct bus_type с методами:
//   match()  — проверить, подходит ли драйвер к устройству
//   probe()  — инициализировать устройство
//   remove() — деинициализировать
//
// Мы начинаем с PCI — это необходимо для GPU.
