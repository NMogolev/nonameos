// =============================================================================
// NoNameOS — Win32 Compatibility Layer
// =============================================================================

//
// ┌─────────────────────────────────────────────────────────────────────┐
// │                    АРХИТЕКТУРА СОВМЕСТИМОСТИ                        │
// │                                                                     │
// │  Как это работает в настоящей Windows:                              │
// │                                                                     │
// │    ┌──────────────────┐                                             │
// │    │   Приложение.exe │   ← Win32 API (CreateFile, CreateWindow)    │
// │    └────────┬─────────┘                                             │
// │             │ вызывает                                              │
// │    ┌────────▼─────────┐                                             │
// │    │   KERNEL32.DLL   │   ← «обёртка» над NT API                    │
// │    │   USER32.DLL     │                                             │
// │    │   GDI32.DLL      │                                             │
// │    └────────┬─────────┘                                             │
// │             │ вызывает                                              │
// │    ┌────────▼─────────┐                                             │
// │    │    NTDLL.DLL     │   ← «мост» в ядро (syscall/int 2E)          │
// │    └────────┬─────────┘                                             │
// │             │ syscall                                               │
// │    ┌────────▼─────────┐                                             │
// │    │   NTOSKRNL.EXE   │   ← NT ядро (Object Mgr, I/O Mgr, ...)      │
// │    │   WIN32K.SYS     │   ← графика + окна (GDI/USER в ядре)        │
// │    └──────────────────┘                                             │
// │                                                                     │
// │  Как это работает в Wine:                                           │
// │                                                                     │
// │    ┌──────────────────┐                                             │
// │    │   Приложение.exe │   ← тот же .exe, без изменений              │
// │    └────────┬─────────┘                                             │
// │             │                                                       │
// │    ┌────────▼─────────┐                                             │
// │    │  wine-kernel32   │   ← реализация Win32 на Linux               │
// │    │  wine-user32     │   ← окна через X11/Wayland                  │
// │    │  wine-gdi32      │   ← рисование через Mesa/Vulkan             │
// │    └────────┬─────────┘                                             │
// │             │                                                       │
// │    ┌────────▼─────────┐                                             │
// │    │  wine-ntdll      │   ← NT API → Linux syscalls                 │
// │    └────────┬─────────┘                                             │
// │             │                                                       │
// │    ┌────────▼─────────┐                                             │
// │    │   Linux Kernel   │   ← POSIX ядро                              │
// │    └──────────────────┘                                             │
// │                                                                     │
// │  Как это работает в ReactOS:                                        │
// │                                                                     │
// │    Полная реимплементация Windows NT с нуля.                        │
// │    ntoskrnl → Object Manager, I/O Manager, Executive                │
// │    win32ss  → Window Manager + GDI (аналог win32k.sys)              │
// │    Всё как в Windows, но open source.                               │
// │                                                                     │
// │  Как это будет в NoNameOS:                                          │
// │                                                                     │
// │    ┌──────────────────┐                                             │
// │    │   Приложение.exe │   ← PE файл, загруженный нашим лоадером     │
// │    └────────┬─────────┘                                             │
// │             │ вызывает наши DLL-заглушки                            │
// │    ┌────────▼─────────┐                                             │
// │    │ compat/kernel32  │   ← наша реализация Win32 API               │
// │    │ compat/user32    │   ← окна (через наш compositor)             │
// │    │ compat/gdi32     │   ← рисование (через наш GPU driver)        │
// │    └────────┬─────────┘                                             │
// │             │ IPC к серверам                                        │
// │    ┌────────▼─────────┐                                             │
// │    │ NT Compat Layer  │   ← Object Manager, NT API                  │
// │    └────────┬─────────┘                                             │
// │             │ syscall                                               │
// │    ┌────────▼─────────┐                                             │
// │    │ NoNameOS Kernel  │   ← наше микроядро                          │
// │    └──────────────────┘                                             │
// │                                                                     │
// │  Ключевое отличие: у нас микроядро, а не монолит.                   │
// │  Object Manager и NT API живут частично в ядре (handle table),      │
// │  частично в user-space серверах (файловая система, реестр).         │
// │  Win32 подсистема (окна, GDI) — полностью в user-space.             │
// └─────────────────────────────────────────────────────────────────────┘
//
// Стратегия:
//
//   1. ТИПЫ (types.rs) — определяем все фундаментальные типы Windows.
//
//   2. ОШИБКИ (error.rs) — NTSTATUS и Win32 error codes.
//      Каждый API возвращает статус. Без этого ни один вызов не работает.
//
//   3. ОБЪЕКТЫ (object.rs) — Object Manager.
//      В Windows БУКВАЛЬНО ВСЁ — объект.
//      Object Manager — фундамент, на котором стоит вся ОС.
//      (Аналог: ReactOS ntoskrnl/ob/, Wine server/object.c)
//
//   4. PE ФОРМАТ (pe.rs) — парсер исполняемых файлов.
//      Без него нельзя загрузить ни один .exe или .dll.
//      (Аналог: Wine dlls/ntdll/loader.c, ReactOS ntoskrnl/mm/section.c)
//
//   5. NT API (ntapi.rs) — «настоящий» API Windows.
//      KERNEL32.CreateFile() внутри вызывает NTDLL.NtCreateFile().
//      Мы реализуем NT API, а Win32 — тонкая обёртка.
//      (Аналог: Wine dlls/ntdll/, ReactOS ntoskrnl/)
//
//   6. ПОДСИСТЕМА (subsys.rs) — каркас для Win32k (окна, GDI).
//      (Аналог: ReactOS win32ss/, Wine dlls/user32/ + dlls/gdi32/)
//
// =============================================================================

#[allow(non_camel_case_types)]
pub mod types;
pub mod error;
pub mod object;
pub mod pe;
pub mod ntapi;
pub mod subsys;
