// =============================================================================
// NoNameOS — Подсистема управления памятью
// =============================================================================
//
// Память в x86_64 устроена в два уровня:
//
//   1. ФИЗИЧЕСКАЯ ПАМЯТЬ (phys.rs)
//      Реальная RAM, разбитая на страницы по 4 КиБ (4096 байт).
//      Аллокатор отслеживает: какие страницы свободны, какие заняты.
//      Использует bitmap — массив битов, где 0 = свободна, 1 = занята.
//
//   2. ВИРТУАЛЬНАЯ ПАМЯТЬ (paging.rs)
//      Каждый процесс видит своё адресное пространство (0x0000... — 0xFFFF...).
//      CPU через таблицы страниц (Page Tables) транслирует виртуальный адрес
//      в физический. Это даёт:
//        - Изоляцию процессов (один не видит память другого)
//        - Можно выделить больше памяти, чем есть физически (swap)
//        - Ядро живёт в верхней части адресного пространства
//
// Цепочка трансляции адреса (4 уровня):
//
//   Виртуальный адрес (48 бит)
//   ├── биты 47..39 → индекс в PML4 (Page Map Level 4)
//   ├── биты 38..30 → индекс в PDPT (Page Directory Pointer Table)
//   ├── биты 29..21 → индекс в PD   (Page Directory)
//   ├── биты 20..12 → индекс в PT   (Page Table)
//   └── биты 11..0  → смещение внутри страницы (4096 байт)
//
//   PML4[i] → PDPT[j] → PD[k] → PT[l] → физический адрес + смещение
//
// В boot.asm мы уже настроили простую карту:
//   - Первый 1 ГиБ виртуального пространства = первый 1 ГиБ физической памяти
//   - Использовали 2 МиБ huge pages (без уровня PT)
//
// Здесь мы переходим на нормальное управление с 4 КиБ страницами.
// =============================================================================

pub mod phys;
pub mod paging;

/// Размер страницы — 4 КиБ (4096 байт). Стандарт для x86_64.
pub const PAGE_SIZE: usize = 4096;

/// Выравнивание адреса вниз до границы страницы.
/// Пример: align_down(0x1234, 4096) = 0x1000
pub const fn align_down(addr: usize, align: usize) -> usize {
    addr & !(align - 1)
}

/// Выравнивание адреса вверх до границы страницы.
/// Пример: align_up(0x1001, 4096) = 0x2000
pub const fn align_up(addr: usize, align: usize) -> usize {
    (addr + align - 1) & !(align - 1)
}
